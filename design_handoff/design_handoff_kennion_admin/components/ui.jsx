function Btn({ children, variant = "default", size, onClick, icon, iconRight, className = "", type, disabled, ...rest }) {
  return (
    <button type={type || "button"} className={`btn btn-${variant} ${size === "sm" ? "btn-sm" : ""} ${className}`} onClick={onClick} disabled={disabled} {...rest}>
      {icon && <Icon name={icon} />}
      {children}
      {iconRight && <Icon name={iconRight} />}
    </button>
  );
}

