function StatusBadge({ status }) {
  const cfg = window.STATUS_OPTIONS.find(s => s.value === status);
  if (!cfg) return null;
  return <Badge variant={cfg.badge} icon={cfg.icon}>{cfg.label}</Badge>;
}

